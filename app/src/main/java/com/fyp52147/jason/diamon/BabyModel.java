package com.fyp52147.jason.diamon;

public class BabyModel {
    public String mBabyName;
    public String mBabyType;
    public String mBabyMom;
    public String mBabyStatus;

    public BabyModel() {}

    public BabyModel(String mBabyName, String mBabyType, String mBabyMom, String mBabyStatus) {
        this.mBabyName = mBabyName;
        this.mBabyType = mBabyType;
        this.mBabyMom = mBabyMom;
        this.mBabyStatus = mBabyStatus;
    }

    public String getmBabyName() {
        return mBabyName;
    }

    public void setmBabyName(String mBabyName) {
        this.mBabyName = mBabyName;
    }

    public String getmBabyType() {
        return mBabyType;
    }

    public void setmBabyType(String mBabyType) {
        this.mBabyType = mBabyType;
    }

    public String getmBabyMom() {
        return mBabyMom;
    }

    public void setmBabyMom(String mBabyMom) {
        this.mBabyMom = mBabyMom;
    }

    public String getmBabyStatus() {
        return mBabyStatus;
    }

    public void setmBabyStatus(String mBabyStatus) {
        this.mBabyStatus = mBabyStatus;
    }

}
